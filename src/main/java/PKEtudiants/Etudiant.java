package PKEtudiants;

public class Etudiant {

	private String id;
	private String nom;
	private String prenom;
	
	public String getId()
	{
		return this.id;
	}
	public String setId(String i)
	{
		return this.id=i;
	}
	public String getNom()
	{
		return this.nom;
	}
	public String setNom(String n)
	{
		return this.nom=n;
	}
	public String getPrenom()
	{
		return this.prenom;
	}
	public String setPrenom(String n)
	{
		return this.prenom=n;
	}
	
}
